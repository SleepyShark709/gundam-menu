interface ModelData {
  id: string;
  series: string;
  number: number;
  name: string;
  nameJa?: string;
  price: number;
  priceTaxFree: number;
  releaseDate: string;
  isLimited: boolean;
  imageUrl: string;
  productUrl: string;
}

async function main(): Promise<void> {
  // Parse --series flag (e.g. --series=hg,pg)
  const seriesArg = process.argv.find((a) => a.startsWith('--series='));
  const series = seriesArg
    ? seriesArg.split('=')[1].split(',').map((s) => s.trim().toLowerCase())
    : ['hg', 'rg', 'mg', 'pg'];

  const hasApiKey = !!(process.env.DEEPSEEK_API_KEY || process.env.ANTHROPIC_API_KEY);
  const apiProvider = process.env.DEEPSEEK_API_KEY ? 'DeepSeek' : 'Anthropic';

  if (!hasApiKey) {
    console.warn(
      '\n⚠  No API key set. AI translation step will be skipped.\n' +
        '   Set DEEPSEEK_API_KEY or ANTHROPIC_API_KEY to enable AI fallback.\n',
    );
  } else {
    console.log(`\n✓ Using ${apiProvider} API for AI translation.\n`);
  }

  let totalModels = 0;
  let totalTranslated = 0;
  let totalRemaining = 0;

  for (const s of series) {
    const filePath = path.join(DATA_DIR, `${s}.json`);

    let rawContent: string;
    try {
      rawContent = await fs.readFile(filePath, 'utf-8');
    } catch {
      console.warn(`[${s}] File not found: ${filePath}, skipping.`);
      continue;
    }

    const data: ModelData[] = JSON.parse(rawContent);
    console.log(`\n[${s}] Processing ${data.length} models...`);

    // ------------------------------------------------------------------
    // Step 1: Rule-based translation
    // ------------------------------------------------------------------
    for (const model of data) {
      // Preserve original Japanese name
      if (!model.nameJa) {
        model.nameJa = model.name;
      }
      // Apply rule-based translation from the Japanese original
      model.name = ruleBasedTranslate(model.nameJa);
    }

    const afterRules = data.filter((m) => !hasKatakana(m.name)).length;
    const needAI = data.filter((m) => hasKatakana(m.name));
    console.log(
      `[${s}] After rule-based: ${afterRules}/${data.length} fully translated, ` +
        `${needAI.length} still contain katakana`,
    );

    // Log samples of remaining katakana names for debugging
    if (needAI.length > 0) {
      const samples = needAI.slice(0, 5);
      console.log(`[${s}] Samples needing AI translation:`);
      for (const sample of samples) {
        console.log(`       "${sample.nameJa}" → "${sample.name}"`);
      }
      if (needAI.length > 5) {
        console.log(`       ... and ${needAI.length - 5} more`);
      }
    }

    // ------------------------------------------------------------------
    // Step 2: AI translation for remaining names
    // ------------------------------------------------------------------
    if (needAI.length > 0 && hasApiKey) {
      console.log(`[${s}] Starting AI translation for ${needAI.length} names...`);

      const batchSize = 50;
      for (let i = 0; i < needAI.length; i += batchSize) {
        const batch = needAI.slice(i, i + batchSize);
        const batchNum = Math.floor(i / batchSize) + 1;
        const totalBatches = Math.ceil(needAI.length / batchSize);
        console.log(
          `[${s}] AI batch ${batchNum}/${totalBatches} (${batch.length} names)...`,
        );

        const items: TranslationItem[] = batch.map((m, j) => ({
          index: i + j,
          original: m.nameJa!,
          partialTranslated: m.name,
        }));

        const translations = await translateBatchWithAI(items);

        // Apply results
        let applied = 0;
        for (let j = 0; j < batch.length; j++) {
          if (translations[j] && !hasKatakana(translations[j]!)) {
            batch[j].name = translations[j]!;
            applied++;
          } else if (translations[j]) {
            // AI returned something but it still has katakana — use it anyway
            // as it's likely a better partial translation
            batch[j].name = translations[j]!;
          }
        }
        console.log(`[${s}] AI batch ${batchNum}: applied ${applied}/${batch.length}`);

        // Rate limiting: wait 1 second between batches
        if (i + batchSize < needAI.length) {
          await new Promise((r) => setTimeout(r, 1000));
        }
      }
    }

    // ------------------------------------------------------------------
    // Save results
    // ------------------------------------------------------------------
    await fs.writeFile(filePath, JSON.stringify(data, null, 2) + '\n', 'utf-8');
    console.log(`[${s}] Saved ${data.length} models to ${filePath}`);

    // ------------------------------------------------------------------
    // Statistics
    // ------------------------------------------------------------------
    const translated = data.filter((m) => !hasKatakana(m.name)).length;
    const remaining = data.filter((m) => hasKatakana(m.name)).length;
    console.log(
      `[${s}] Final: ${translated} fully translated, ${remaining} still contain katakana`,
    );

    totalModels += data.length;
    totalTranslated += translated;
    totalRemaining += remaining;
  }

  // ------------------------------------------------------------------
  // Summary
  // ------------------------------------------------------------------
  console.log('\n' + '='.repeat(60));
  console.log('Translation Summary');
  console.log('='.repeat(60));
  console.log(`Total models:      ${totalModels}`);
  console.log(`Fully translated:  ${totalTranslated} (${((totalTranslated / totalModels) * 100).toFixed(1)}%)`);
  console.log(`Remaining katakana: ${totalRemaining} (${((totalRemaining / totalModels) * 100).toFixed(1)}%)`);
  console.log('='.repeat(60));

  if (totalRemaining > 0 && !hasApiKey) {
    console.log(
      '\nTip: Set DEEPSEEK_API_KEY or ANTHROPIC_API_KEY to enable AI translation for remaining names.',
    );
  }
}

// ---------------------------------------------------------------------------
// Entry point
// ---------------------------------------------------------------------------

main().catch((err) => {
  console.error('Fatal error:', err);
  process.exit(1);
});

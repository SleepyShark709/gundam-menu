// Helper: sort mapping by key length descending (longest match first)
// ---------------------------------------------------------------------------

const SORTED_MAP = [...JA_ZH_MAP].sort((a, b) => b[0].length - a[0].length);

// ---------------------------------------------------------------------------
// Rule-based translation
// ---------------------------------------------------------------------------

/**
 * Apply all rule-based replacements to a name string.
 * Replaces longest matches first to avoid substring conflicts.
 */
function ruleBasedTranslate(name: string): string {
  let result = name;
  for (const [ja, zh] of SORTED_MAP) {
    // Use split+join for global replacement (no regex escaping needed)
    if (result.includes(ja)) {
      result = result.split(ja).join(zh);
    }
  }
  return result;
}


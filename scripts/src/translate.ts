// AI translation (Claude API fallback)
// ---------------------------------------------------------------------------

interface TranslationItem {
  index: number;
  original: string;
  partialTranslated: string;
}

/**
 * Translate a batch of names using DeepSeek API (OpenAI-compatible).
 * Falls back to Anthropic Claude API if ANTHROPIC_API_KEY is set instead.
 * Returns an array of translated strings in the same order.
 */
async function translateBatchWithAI(
  items: TranslationItem[],
): Promise<(string | null)[]> {
  const promptText = `你是高达(Gundam)系列的翻译专家。请将以下日文高达模型名称翻译为中文。
规则：
1. 保留英文型号前缀（如 RG 1/144, MG 1/100, HG 1/144, PG 1/60, HGUC 1/144）和机体编号（如 RX-78-2, MSN-04）
2. 使用中国大陆约定俗成的官方/通用中文翻译
3. 保留 Ver.Ka, Ver.2.0 等版本号
4. 每行输出一个翻译结果，与输入顺序一一对应
5. 只输出翻译后的名称，不要编号，不要解释，不要空行

待翻译：
${items.map((n, i) => `${i + 1}. ${n.partialTranslated}`).join('\n')}`;

  try {
    let text: string;

    if (process.env.DEEPSEEK_API_KEY) {
      // DeepSeek API (OpenAI-compatible)
      const res = await fetch('https://api.deepseek.com/chat/completions', {
        method: 'POST',
        headers: {
          'Content-Type': 'application/json',
          'Authorization': `Bearer ${process.env.DEEPSEEK_API_KEY}`,
        },
        body: JSON.stringify({
          model: 'deepseek-chat',
          max_tokens: 4096,
          messages: [{ role: 'user', content: promptText }],
        }),
      });

      if (!res.ok) {
        const errBody = await res.text();
        throw new Error(`DeepSeek API ${res.status}: ${errBody}`);
      }

      const data = await res.json() as { choices: { message: { content: string } }[] };
      text = data.choices[0]?.message?.content ?? '';
    } else if (process.env.ANTHROPIC_API_KEY) {
      // Anthropic Claude API fallback
      let Anthropic: typeof import('@anthropic-ai/sdk').default;
      try {
        const mod = await import('@anthropic-ai/sdk');
        Anthropic = mod.default;
      } catch {
        console.warn('  [ai] @anthropic-ai/sdk is not installed.');
        return items.map(() => null);
      }

      const client = new Anthropic();
      const response = await client.messages.create({
        model: 'claude-sonnet-4-20250514',
        max_tokens: 4096,
        messages: [{ role: 'user', content: promptText }],
      });

      text = response.content
        .filter((block): block is { type: 'text'; text: string } => block.type === 'text')
        .map((block) => block.text)
        .join('\n');
    } else {
      return items.map(() => null);
    }

    // Parse line-by-line
    const lines = text
      .split('\n')
      .map((l) => l.trim())
      .filter((l) => l.length > 0);

    // Remove potential numbering (e.g. "1. xxx" -> "xxx")
    const cleaned = lines.map((l) => l.replace(/^\d+\.\s*/, ''));

    // Map back; if count mismatch, return what we have
    return items.map((_, i) => (i < cleaned.length ? cleaned[i] : null));
  } catch (err) {
    console.error(
      `  [ai] API call failed: ${err instanceof Error ? err.message : String(err)}`,
    );
    return items.map(() => null);
  }
}

